<dl>
<dt>How do I submit a news/event announcement for consideration in Today@UNL?</dt>
<dd>
    <p>From <a href="<?php echo UNL_ENews_Controller::getURL(); ?>">UNL Announce</a>, select <a href="<?php echo UNL_ENews_Controller::getURL(); ?>?view=submit">Submit A News Item</a></p>
    <ul>
        <li>Is your item a News Announcement or an Event Announcement?</li>
        <li>Examples of news: grant, honor, award, feature story, discovery, new project</li>
        <li>Examples of events:  Anything with a date/time/place attached to it: Lecture, seminar, conference, performance, concert, show, program, open house, reception</li>
    </ul>
</dd>

<dt>What if I think it's both?</dt>
<dd>
    Sure. Like announcing a series of lectures or tickets/reservations open for some future event?
    <ul>
        <li>Enter it as a News Announcement, then add the specific dates and topics into the Events Announcement</li>
        <li>Look at the submission forms when you click on each; they are slightly different. The News Announcement gives you more detail areas</li>
    </ul>
</dd>

<dt>Under the Event Announcement submission, it asks me "Which Event" when I entered the date. Why?</dt>
<dd>
Either enter the event as a new event, or, before you get to this screen, enter your event in the Events calendar &ndash; this interface pulls events from the <a href="http://events.unl.edu/">Events calendar</a>
</dd>

<dt>What's a summary and how long is a summary?</dt> 
<dd>
Ideally a summary is the few sentences that will appear in the e-newsletter that serves as a teaser/mini headline to get readers interested or give them the gist and no more. As you type, you will see the summary appear in the box on the right. The best summaries are very short &ndash; one or two sentences &ndash; and give the basic facts. This interface limits summaries to 300 characters.
</dd>


<dt>Full article: What does that mean?</dt>
<dd>
    <p>Say you have an entire news release or feature story that you want to submit.</p> 
    <ul>
        <li>Copy and paste it into "Full Article" </li>
        <li>This gives editors more that they can use in other ways and in other channels: UNL Today, Scarlet, or as a news release or pitch. If you've written it, submit it. Use the summary box for the edited/shortened version</li>
    </ul>
</dd>

<dt>You're asking when I want this to run. I get to choose?</dt>
<dd>
    <ul>
        <li>Your suggestions are helpful, and particularly useful if there is a deadline for entering, or you desire your event announcement to precede your event by a few days to give people time to plan to attend.</li> 
        <li>Keep in mind that space and time constraints will dictate some of these decisions; we'll do our best to honor requests</li>
        <li>Also remember that if you've entered your event in the UNL calendar, the events of that day will be part of the daily calendar</li>
        <li>The "last date this could run" typically is the last day before your event is over</li>
    </ul>
</dd>

<dt>Can I ask to run events and news on several days?</dt>
<dd>Once is enough because nobody wants to read the same announcements every day. If you have a series of events, make sure there is new information entered for every new day: a new title, speaker, location, etc.
</dd> 

<dt>Sponsoring unit: What does that mean? What if I'm submitting something for some other unit?</dt>
<dd>
    <ul>
        <li>The system reads who you are when you enter, and defaults to your "unit" after you've entered information.</li>
        <li>If you're entering something for another unit, change it so it's accurate</li>
    </ul>
</dd>

<dt>Image upload: What type and size of image works best?</dt>
<dd>
    <ul>
        <li>Photos with action or plenty of visual appeal are best.</li>
        <li>If you have a speaker or event highlighting one or two people, submit "head and shoulders" shots</li>
        <li>Provide good quality color photos, saved as RGB jpeg/jpg images and have them already tightly cropped, in good focus with good contrast.</li> 
        <li>Type treatments, word marks and logos (university-approved) will be considered</li>
    </ul>
</dd>

<dt>What's the deadline for submissions?</dt>
<dd>
    <ul>
        <li>During the summer we'll e-mail twice a week (Tuesdays and Fridays) &ndash; submit items no later than noon the prior day </li>
        <li>During the fall, delivery will be every day Monday-Friday (not university holidays) &ndash; submit items no later than noon the prior day</li>
    </ul>
</dd>

